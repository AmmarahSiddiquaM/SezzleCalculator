const Add = (OldValue,NewValue) => {
    return OldValue + NewValue
} 
const Sub = (OldValue,NewValue) => {
    return OldValue - NewValue
}

const Mul = (OldValue,NewValue) => {
    return OldValue*NewValue
}

const Div = (OldValue,NewValue) => {
    return OldValue/NewValue
}

const ModDiv = (OldValue,NewValue) => {
    return OldValue%NewValue
}
